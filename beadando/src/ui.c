static void ui_begin_2d(int w, int h){
    glMatrixMode(GL_PROJECTION);
    glPushMatrix();
    glLoadIdentity();
    glOrtho(0.0, (double)w, (double)h, 0.0, -1.0, 1.0);
    glMatrixMode(GL_MODELVIEW);
    glPushMatrix();
    glLoadIdentity();
    glDisable(GL_DEPTH_TEST);
    glDisable(GL_CULL_FACE);
}

static void ui_end_2d(void){
    glEnable(GL_CULL_FACE);
    glEnable(GL_DEPTH_TEST);
    glMatrixMode(GL_MODELVIEW);
    glPopMatrix();
    glMatrixMode(GL_PROJECTION);
    glPopMatrix();
}

static void draw_char(int x, int y, char c, int scale){
    unsigned char uc = (unsigned char)c;
    if(uc < 32 || uc > 127){
        uc = (unsigned char)'?';
    }

    const unsigned char *glyph = font8x8_basic[(int)uc - 32];

    for(int row = 0; row < 8; row++){
        unsigned char bits = glyph[row];
        for(int col = 0; col < 8; col++){
            int on = (bits & (1u << (7 - col))) != 0;
            if(!on){
                continue;
            }

            int px = x + col * scale;
            int py = y + row * scale;

            glBegin(GL_QUADS);
            glVertex2i(px, py);
            glVertex2i(px + scale, py);
            glVertex2i(px + scale, py + scale);
            glVertex2i(px, py + scale);
            glEnd();
        }
    }
}

static void draw_text(int x, int y, const char *text, int scale){
    int cx = x;
    int cy = y;

    for(size_t i = 0; text[i] != '\0'; i++){
        char c = text[i];
        if(c == '\n'){
            cy += 10 * scale;
            cx = x;
            continue;
        }
        draw_char(cx, cy, c, scale);
        cx += 8 * scale;
    }
}

void ui_init(UI *ui){
    ui->show_help = true;
    ui->status_text[0] = '\0';
    ui->status_timer = 0.0f;
}

void ui_toggle_help(UI *ui){
    ui->show_help = !ui->show_help;
}

void ui_set_status(UI *ui, const char *text, float seconds){
    if(text == NULL){
        ui->status_text[0] = '\0';
        ui->status_timer = 0.0f;
        return;
    }

    strncpy(ui->status_text, text, sizeof(ui->status_text) - 1);
    ui->status_text[sizeof(ui->status_text) - 1] = '\0';
    ui->status_timer = seconds;
}

void ui_update(UI *ui, float dt){
    if(ui->status_timer > 0.0f){
        ui->status_timer -= dt;
        if(ui->status_timer <= 0.0f){
            ui->status_timer = 0.0f;
            ui->status_text[0] = '\0';
        }
    }
}

void ui_render_help_overlay(const UI *ui, int screen_w, int screen_h){
    ui_begin_2d(screen_w, screen_h);

    if(ui->show_help){
        int panel_x = 16;
        int panel_y = 16;
        int panel_w = 620;
        int panel_h = 220;

        glColor4f(0.0f, 0.0f, 0.0f, 0.65f);
        glBegin(GL_QUADS);
        glVertex2i(panel_x, panel_y);
        glVertex2i(panel_x + panel_w, panel_y);
        glVertex2i(panel_x + panel_w, panel_y + panel_h);
        glVertex2i(panel_x, panel_y + panel_h);
        glEnd();

        glColor3f(0.9f, 0.9f, 0.95f);
        draw_text(panel_x + 12, panel_y + 12,
            "Solar System Explorer\n"
            "\n"
            "Controls:\n"
            "  Mouse: look around\n"
            "  Left / Right: select planet\n"
            "  Enter: land on selected planet\n"
            "  W/A/S/D: move on surface after landing\n"
            "  E: interact with nearby object\n"
            "  Left click: ship / throw stone on Earth\n"
            "  Backspace: return to overview\n"
            "  F1: toggle help\n"
            "  Esc: quit\n"
            "\n"
            "Gas giants cannot be landed on.",
            2);
    }

    if(ui->status_timer > 0.0f && ui->status_text[0] != '\0'){
        int panel_w = 560;
        int panel_h = 42;
        int panel_x = (screen_w - panel_w) / 2;
        int panel_y = screen_h - panel_h - 24;

        glColor4f(0.0f, 0.0f, 0.0f, 0.78f);
        glBegin(GL_QUADS);
        glVertex2i(panel_x, panel_y);
        glVertex2i(panel_x + panel_w, panel_y);
        glVertex2i(panel_x + panel_w, panel_y + panel_h);
        glVertex2i(panel_x, panel_y + panel_h);
        glEnd();

        glColor3f(1.0f, 0.85f, 0.30f);
        draw_text(panel_x + 14, panel_y + 10, ui->status_text, 2);
    }

    ui_end_2d();
}